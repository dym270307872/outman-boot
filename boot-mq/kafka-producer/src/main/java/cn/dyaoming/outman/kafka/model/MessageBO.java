package cn.dyaoming.outman.kafka.model;


public class MessageBO {
	private String type;
	private String id;
	private String info;
    public String getType() {
        return type;
    }
    public void setType(String type) {
        this.type = type;
    }
    public String getId() {
        return id;
    }
    public void setId(String id) {
        this.id = id;
    }
    public String getInfo() {
        return info;
    }
    public void setInfo(String info) {
        this.info = info;
    }
	
	
}